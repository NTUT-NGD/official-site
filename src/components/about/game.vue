<template>
  <v-app>
    <canvas
      ref="board"
      width="640"
      height="480"
      style="border: 1px solid black;"
    ></canvas>
    <br />
    <v-row>
      <v-col cols="10">
        <v-btn v-on:click="move('right')" color="Secondary" text>Right</v-btn>
        <v-btn v-on:click="move('left')" color="Secondary" text>left</v-btn>
        <v-btn v-on:click="move('up')" color="Secondary" text>up</v-btn>
        <v-btn v-on:click="move('down')" color="Secondary" text>down</v-btn>
      </v-col>
      <v-col cols="1">
        <v-btn @click="reset()" color="Secondary" text>Reset</v-btn>
      </v-col>
    </v-row>
    <v-row>
      <v-col cols="12">
        <div style="border: 1px dashed black;" class="pa-5">
          <p>Dialog</p>
          <p>{{ dialog }}</p>
        </div>
      </v-col>
    </v-row>
  </v-app>
</template>

<script>
// import io from "socket.io-client";
export default {
  name: "about",
  components: {},
  data() {
    return {
      dialog:
        "????????????????????????????????????????????????????????????????????????",
      socket: {},
      context: {},
      position: {
        x: 200,
        y: 200,
        w: 20,
        h: 20,
        originX: 200,
        originY: 200
      },
      builds: [
        { x: 0, y: 0, w: 50, h: 50, infos: "北科遊戲設計社" },
        { x: 100, y: 100, w: 50, h: 50, infos: "成立時間：108-2, 03/15" }
      ]
    };
  },
  created() {
    // this.socket = io("http://localhost:3000");
  },
  methods: {
    move(direction) {
      let movement = 20;
      switch (direction) {
        case "right":
          this.position.x += movement;
          break;
        case "left":
          this.position.x -= movement;
          break;
        case "up":
          this.position.y -= movement;
          break;
        case "down":
          this.position.y += movement;
          break;
      }
      this.collidingDetect();
      this.doDraw();
    },
    reset() {
      this.position.x = this.position.originX;
      this.position.y = this.position.originY;
      this.doDraw();
    },
    collidingDetect() {
      let trigger = false;
      this.builds.forEach(element => {
        if (this.rectsColliding(this.position, element)) {
          this.dialog = element.infos;
          trigger = true;
        }
      });
      if (trigger == false)
        this.dialog =
          "????????????????????????????????????????????????????????????????????????";
    },
    doDraw() {
      this.context = this.$refs.board.getContext("2d");
      this.context.clearRect(
        0,
        0,
        this.$refs.board.width,
        this.$refs.board.height
      );
      this.context.fillStyle = "#FFFFFF";
      this.context.fillRect(
        0,
        0,
        this.$refs.board.width,
        this.$refs.board.width
      );
      this.builds.forEach(element => {
        this.context.fillStyle = "#3949ab";
        this.context.fillRect(element.x, element.y, element.w, element.h);
      });
      this.context.fillStyle = "#ffb300";
      this.context.fillRect(
        this.position.x,
        this.position.y,
        this.position.w,
        this.position.h
      );
    },
    rectsColliding(rect1, rect2) {
      return (
        rect1.x < rect2.x + rect2.w &&
        rect1.x + rect1.w > rect2.x &&
        rect1.y < rect2.y + rect2.h &&
        rect1.y + rect1.h > rect2.y
      );
    }
  },
  mounted() {
    this.$store.commit("setActivedPage", "/about");
    document.title = "About | NGC";
    this.doDraw();
  },
  computed: {}
};
</script>
